/// One paper-trade position in the `paper_positions` table.
/// `side` is `'long'` or `'short'`.
/// `closed_at` and `close_price` are `None` while the position is open.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct PaperPosition {
    pub id: String,
    pub symbol: String,
    /// `'long'` | `'short'`
    pub side: String,
    pub qty: f64,
    pub ref_price: f64,
    /// Unix milliseconds.
    pub opened_at: i64,
    /// Unix milliseconds. `None` while open.
    pub closed_at: Option<i64>,
    pub close_price: Option<f64>,
}

/// One fill event in the `paper_fills` table.
/// `kind` is `'open'`, `'close'`, or `'adjust'`.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct PaperFill {
    pub id: String,
    pub position_id: String,
    /// Unix milliseconds.
    pub ts: i64,
    pub qty: f64,
    pub price: f64,
    /// `'open'` | `'close'` | `'adjust'`
    pub kind: String,
}

/// Open a new paper position and insert the corresponding `'open'` fill
/// atomically.  Returns the inserted position.
///
/// `fill_id` must be a caller-generated UUID for the fill row.
pub fn paper_position_open(
    conn: &Connection,
    position: &PaperPosition,
    fill_id: &str,
) -> rusqlite::Result<()> {
    let tx = conn.unchecked_transaction()?;
    conn.execute(
        "INSERT INTO paper_positions
             (id, symbol, side, qty, ref_price, opened_at, closed_at, close_price)
         VALUES (?1, ?2, ?3, ?4, ?5, ?6, ?7, ?8)",
        params![
            position.id,
            position.symbol,
            position.side,
            position.qty,
            position.ref_price,
            position.opened_at,
            position.closed_at,
            position.close_price,
        ],
    )?;
    // Insert the matching open fill.
    conn.execute(
        "INSERT INTO paper_fills (id, position_id, ts, qty, price, kind)
         VALUES (?1, ?2, ?3, ?4, ?5, ?6)",
        params![
            fill_id,
            position.id,
            position.opened_at,
            position.qty,
            position.ref_price,
            "open",
        ],
    )?;
    tx.commit()?;
    Ok(())
}

/// Close an open position by setting `closed_at` + `close_price` and
/// inserting a `'close'` fill.  `fill_id` is the fill row UUID.
pub fn paper_position_close(
    conn: &Connection,
    position_id: &str,
    close_price: f64,
    fill_id: &str,
) -> rusqlite::Result<PaperPosition> {
    let tx = conn.unchecked_transaction()?;

    // Fetch the open position — fails with QueryReturnedNoRows if unknown.
    let pos = paper_position_get(conn, position_id)?
        .ok_or(rusqlite::Error::QueryReturnedNoRows)?;

    let closed_at = now_ms();

    conn.execute(
        "UPDATE paper_positions SET closed_at = ?1, close_price = ?2 WHERE id = ?3",
        params![closed_at, close_price, position_id],
    )?;
    conn.execute(
        "INSERT INTO paper_fills (id, position_id, ts, qty, price, kind)
         VALUES (?1, ?2, ?3, ?4, ?5, ?6)",
        params![fill_id, position_id, closed_at, pos.qty, close_price, "close"],
    )?;

    tx.commit()?;

    let updated = paper_position_get(conn, position_id)?
        .expect("position disappeared after close");
    Ok(updated)
}

