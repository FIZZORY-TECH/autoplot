//! src-tauri/src/ai_workspace.rs — DAO layer for AI workspace tables (Step 2)
//!
//! Tables introduced by migration 0010_ai_workspace_tables.sql:
//!   `research_notes`, `ai_datasets`, `ai_strategies`, `strategy_revisions`,
//!   `paper_positions`, `paper_fills`.
//!
//! **No Tauri commands live here.** This is pure data-access logic that will be
//! consumed by the MCP bridge (Step 4 / Step 6) and the Strategy Artifact Panel
//! (Step 11b).  The public surface follows the same conventions as
//! `commands/db.rs`:
//!   - Each table exposes insert / get / list / update / delete helpers.
//!   - All functions take `&rusqlite::Connection` and propagate errors via `?`.
//!   - Structs derive `Debug`, `Clone`, `Serialize`, `Deserialize`.
//!   - Time stamps are unix milliseconds (i64), consistent with other tables.
//!
//! **Append-only invariant (ADR-0005):** `update_strategy` MUST insert a new
//! `strategy_revisions` row with `rev = current_revision + 1` and then UPDATE
//! `ai_strategies`.  It MUST NOT mutate any existing `strategy_revisions` row.

// All public items in this module are pre-wired for the MCP bridge (Step 4/6)
// and Strategy Artifact Panel (Step 11b); they are intentionally unused until
// those steps wire them into commands/. Suppressing dead_code for the whole
// module avoids noisy per-item attributes.
#![allow(dead_code)]

use rusqlite::{Connection, params};
use serde::{Deserialize, Serialize};

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

fn now_ms() -> i64 {
    std::time::SystemTime::now()
        .duration_since(std::time::UNIX_EPOCH)
        .map(|d| d.as_millis() as i64)
        .unwrap_or(0)
}

