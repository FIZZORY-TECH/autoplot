/// One research note, optionally tagged and bound to a symbol + timeframe.
/// Mirrors the `research_notes` table introduced in migration 0010.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ResearchNote {
    pub id: String,
    pub title: String,
    pub body: String,
    /// JSON array string, e.g. `'["DeFi","momentum"]'`.
    pub tags_json: String,
    pub symbol: Option<String>,
    pub timeframe: Option<String>,
    /// Unix milliseconds.
    pub created_at: i64,
}

/// Insert a new research note. `id` must be a stable UUID generated by the
/// caller (the TS side or the MCP bridge).
pub fn research_note_insert(conn: &Connection, note: &ResearchNote) -> rusqlite::Result<()> {
    conn.execute(
        "INSERT INTO research_notes (id, title, body, tags_json, symbol, timeframe, created_at)
         VALUES (?1, ?2, ?3, ?4, ?5, ?6, ?7)",
        params![
            note.id,
            note.title,
            note.body,
            note.tags_json,
            note.symbol,
            note.timeframe,
            note.created_at,
        ],
    )?;
    Ok(())
}

/// Return all research notes ordered by `created_at DESC`.
pub fn research_note_list(conn: &Connection) -> rusqlite::Result<Vec<ResearchNote>> {
    let mut stmt = conn.prepare(
        "SELECT id, title, body, tags_json, symbol, timeframe, created_at
         FROM research_notes
         ORDER BY created_at DESC",
    )?;
    let rows = stmt.query_map([], |row| {
        Ok(ResearchNote {
            id: row.get(0)?,
            title: row.get(1)?,
            body: row.get(2)?,
            tags_json: row.get(3)?,
            symbol: row.get(4)?,
            timeframe: row.get(5)?,
            created_at: row.get(6)?,
        })
    })?
    .collect::<rusqlite::Result<Vec<_>>>()?;
    Ok(rows)
}

/// Fetch one note by primary key. Returns `None` when the id is unknown.
pub fn research_note_get(conn: &Connection, id: &str) -> rusqlite::Result<Option<ResearchNote>> {
    let result = conn.query_row(
        "SELECT id, title, body, tags_json, symbol, timeframe, created_at
         FROM research_notes WHERE id = ?1",
        params![id],
        |row| {
            Ok(ResearchNote {
                id: row.get(0)?,
                title: row.get(1)?,
                body: row.get(2)?,
                tags_json: row.get(3)?,
                symbol: row.get(4)?,
                timeframe: row.get(5)?,
                created_at: row.get(6)?,
            })
        },
    );
    match result {
        Ok(r) => Ok(Some(r)),
        Err(rusqlite::Error::QueryReturnedNoRows) => Ok(None),
        Err(e) => Err(e),
    }
}

/// Delete a research note by id. No-op when id doesn't exist.
pub fn research_note_delete(conn: &Connection, id: &str) -> rusqlite::Result<()> {
    conn.execute("DELETE FROM research_notes WHERE id = ?1", params![id])?;
    Ok(())
}

