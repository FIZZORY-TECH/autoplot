/// Head record for a strategy. `body_json` always holds the DSL JSON of
/// `current_revision`. `strategy_revisions` holds the full history.
///
/// Mirrors the `ai_strategies` table (migration 0010).
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct AiStrategy {
    pub id: String,
    pub name: String,
    /// JSON-encoded Strategy DSL (see `src/ai/schemas.ts` for the shape).
    pub body_json: String,
    pub current_revision: i64,
    /// Unix milliseconds.
    pub created_at: i64,
    /// Unix milliseconds — updated on every `update_strategy` call.
    pub updated_at: i64,
}

/// One immutable snapshot in the `strategy_revisions` table.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct StrategyRevision {
    pub id: String,
    pub strategy_id: String,
    pub rev: i64,
    pub body_json: String,
    /// Unix milliseconds.
    pub created_at: i64,
}

/// Insert a brand-new strategy and its first revision (`rev = 1`) atomically.
///
/// The caller supplies a pre-generated UUID for both `strategy.id` and
/// `rev_id` (the revision row's primary key).
pub fn strategy_insert(
    conn: &Connection,
    strategy: &AiStrategy,
    rev_id: &str,
) -> rusqlite::Result<()> {
    // Wrap both inserts in a single transaction so a crash can't leave
    // `ai_strategies` without its matching revision row.
    let tx = conn.unchecked_transaction()?;
    conn.execute(
        "INSERT INTO ai_strategies
             (id, name, body_json, current_revision, created_at, updated_at)
         VALUES (?1, ?2, ?3, ?4, ?5, ?6)",
        params![
            strategy.id,
            strategy.name,
            strategy.body_json,
            strategy.current_revision,
            strategy.created_at,
            strategy.updated_at,
        ],
    )?;
    conn.execute(
        "INSERT INTO strategy_revisions (id, strategy_id, rev, body_json, created_at)
         VALUES (?1, ?2, ?3, ?4, ?5)",
        params![
            rev_id,
            strategy.id,
            strategy.current_revision,
            strategy.body_json,
            strategy.created_at,
        ],
    )?;
    tx.commit()?;
    Ok(())
}

