    /// ADR-0005 invariant: N edits produce N+1 revision rows.
    #[test]
    fn test_update_strategy_produces_n_plus_one_revisions() {
        let conn = fresh_db();
        let body_v1 = sample_strategy_json("s2");
        let strat = make_strategy("s2", "Donchian Breakout", &body_v1);
        strategy_insert(&conn, &strat, "rev-s2-1").expect("insert");

        // Edit 1 → revision 2.
        let body_v2 = body_v1.replace("RSI Mean-Revert", "Donchian v2");
        let updated = strategy_update(&conn, "s2", &body_v2, "rev-s2-2").expect("update 1");
        assert_eq!(updated.current_revision, 2);
        assert!(updated.body_json.contains("Donchian v2"));

        // Edit 2 → revision 3.
        let body_v3 = body_v2.replace("Donchian v2", "Donchian v3");
        let updated2 = strategy_update(&conn, "s2", &body_v3, "rev-s2-3").expect("update 2");
        assert_eq!(updated2.current_revision, 3);

        // N = 2 edits → N+1 = 3 total revision rows.
        let revs = strategy_revisions_list(&conn, "s2").expect("revisions");
        assert_eq!(revs.len(), 3, "2 edits → 3 revision rows (N+1 invariant)");
        assert_eq!(revs[0].rev, 1);
        assert_eq!(revs[1].rev, 2);
        assert_eq!(revs[2].rev, 3);
    }

    /// Append-only: existing revision rows must never be mutated.
    #[test]
    fn test_update_strategy_does_not_mutate_old_revisions() {
        let conn = fresh_db();
        let body_v1 = sample_strategy_json("s3");
        strategy_insert(
            &conn,
            &make_strategy("s3", "Volatile RSI", &body_v1),
            "rev-s3-1",
        )
        .expect("insert");

        let body_v2 = body_v1.replace("RSI Mean-Revert", "Volatile RSI v2");
        strategy_update(&conn, "s3", &body_v2, "rev-s3-2").expect("update");

        let revs = strategy_revisions_list(&conn, "s3").expect("revisions");
        // Rev 1 still holds the original body — immutable.
        assert!(
            revs[0].body_json.contains("RSI Mean-Revert"),
            "rev 1 must remain unchanged after update"
        );
        // Rev 2 holds the new body.
        assert!(revs[1].body_json.contains("Volatile RSI v2"));
    }

    /// Deleting a strategy must cascade to its revision rows (FK ON DELETE CASCADE).
    #[test]
    fn test_delete_strategy_cascades_to_revisions() {
        let conn = fresh_db();
        let body = sample_strategy_json("s4");
        strategy_insert(
            &conn,
            &make_strategy("s4", "Cascade Test", &body),
            "rev-s4-1",
        )
        .expect("insert");
        strategy_update(&conn, "s4", &body.replace("RSI Mean-Revert", "v2"), "rev-s4-2")
            .expect("update");

        // Sanity: 2 revisions before delete.
        let before = strategy_revisions_list(&conn, "s4").expect("before");
        assert_eq!(before.len(), 2);

        // Delete the strategy head → must cascade.
        strategy_delete(&conn, "s4").expect("delete");

        // The head row is gone.
        assert!(strategy_get(&conn, "s4").expect("get").is_none());

        // All revision rows must be gone too.
        let after: i64 = conn
            .query_row(
                "SELECT COUNT(*) FROM strategy_revisions WHERE strategy_id = 's4'",
                [],
                |r| r.get(0),
            )
            .expect("count revisions");
        assert_eq!(after, 0, "cascade delete must remove all revision rows");
    }

